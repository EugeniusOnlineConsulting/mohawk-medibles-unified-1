// ─── Helpers ─────────────────────────────────────────────

async function apiFetch<T>(params?: string): Promise<T> {
    const url = `/api/admin/cart-recovery/${params ? `?${params}` : ""}`;
    const res = await fetch(url);
    if (!res.ok) throw new Error(`API error ${res.status}`);
    return res.json();
}

async function apiPost<T>(body: Record<string, unknown>): Promise<T> {
    const res = await fetch("/api/admin/cart-recovery/", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(body),
    });
    if (!res.ok) throw new Error(`API error ${res.status}`);
    return res.json();
}

function showToast(msg: string, type: "success" | "error" = "success") {
    // Simple toast fallback — replace with sonner if available
    if (typeof window !== "undefined") {
        const el = document.createElement("div");
        el.textContent = msg;
        el.className = `fixed top-4 right-4 z-[9999] px-4 py-3 rounded-xl text-sm font-semibold shadow-lg transition-opacity ${
            type === "success"
                ? "bg-green-500/90 text-black"
                : "bg-red-500/90 text-white"
        }`;
        document.body.appendChild(el);
        setTimeout(() => {
            el.style.opacity = "0";
            setTimeout(() => el.remove(), 300);
        }, 3000);
    }
}

