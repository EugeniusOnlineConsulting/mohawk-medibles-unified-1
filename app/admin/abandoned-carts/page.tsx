function EmailsPanel() {
    const [emails, setEmails] = useState<RecoveryEmail[] | null>(null);
    const [loading, setLoading] = useState(true);

    useEffect(() => {
        apiFetch<RecoveryEmail[]>("action=emails&limit=50")
            .then(setEmails)
            .catch(() => setEmails([]))
            .finally(() => setLoading(false));
    }, []);

    if (loading) {
        return (
            <div className="flex items-center justify-center py-12">
                <RefreshCw size={20} className="animate-spin text-zinc-500" />
            </div>
        );
    }

    if (!emails || emails.length === 0) {
        return (
            <div className={`${cardClass} p-8 text-center`}>
                <Mail size={32} className="text-zinc-500 mx-auto mb-3" />
                <p className="font-bold mb-1">No Recovery Emails Sent Yet</p>
                <p className="text-zinc-500 text-xs">
                    Recovery emails will appear here once the cart abandonment processor runs and sends emails to customers.
                </p>
            </div>
        );
    }

    const statusColors: Record<string, string> = {
        sent: "bg-blue-500/10 text-blue-400",
        delivered: "bg-green-500/10 text-green-400",
        opened: "bg-purple-500/10 text-purple-400",
        clicked: "bg-amber-500/10 text-amber-400",
        converted: "bg-green-500/10 text-green-400",
        failed: "bg-red-500/10 text-red-400",
    };

    return (
        <div className={`${cardClass} overflow-hidden`}>
            <div className="p-4 border-b border-white/5">
                <h3 className="text-sm font-bold uppercase tracking-wider">Recovery Email Log</h3>
                <p className="text-zinc-500 text-xs mt-1">All cart recovery emails sent to customers</p>
            </div>
            <div className="overflow-x-auto">
                <table className="w-full text-xs">
                    <thead>
                        <tr className="border-b border-white/5 bg-white/[0.02]">
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">#</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Recipient</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Subject</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Email</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Discount</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Cart</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Variant</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Status</th>
                            <th className="text-left px-4 py-2 text-zinc-500 uppercase tracking-wider font-bold">Sent</th>
                        </tr>
                    </thead>
                    <tbody>
                        {emails.map((email) => (
                            <tr key={email.id} className="border-b border-white/5 hover:bg-white/[0.02]">
                                <td className="px-4 py-2">
                                    <div
                                        className={`w-6 h-6 rounded-full flex items-center justify-center text-[10px] font-bold ${
                                            email.emailNumber === 1
                                                ? "bg-blue-500/20 text-blue-400"
                                                : email.emailNumber === 2
                                                  ? "bg-amber-500/20 text-amber-400"
                                                  : "bg-red-500/20 text-red-400"
                                        }`}
                                    >
                                        {email.emailNumber}
                                    </div>
                                </td>
                                <td className="px-4 py-2">
                                    <p className="font-bold">{email.userName || "Unknown"}</p>
                                    <p className="text-zinc-500">{email.recipientEmail}</p>
                                </td>
                                <td className="px-4 py-2 text-zinc-400 max-w-[200px] truncate" title={email.subject}>
                                    {email.subject}
                                </td>
                                <td className="px-4 py-2 text-zinc-500">Email {email.emailNumber}</td>
                                <td className="px-4 py-2">
                                    {email.discountCode ? (
                                        <div>
                                            <span className="text-green-400 font-mono font-bold">{email.discountCode}</span>
                                            <p className="text-zinc-500 text-[10px]">{email.discountPercent}% off</p>
                                        </div>
                                    ) : (
                                        <span className="text-zinc-600">---</span>
                                    )}
                                </td>
                                <td className="px-4 py-2 text-green-400 font-bold">${Number(email.cartTotal).toFixed(2)}</td>
                                <td className="px-4 py-2">
                                    {email.subjectVariant === "b" ? (
                                        <span className="px-2 py-0.5 rounded text-[10px] font-bold uppercase bg-green-500/10 text-green-400">B</span>
                                    ) : (
                                        <span className="px-2 py-0.5 rounded text-[10px] font-bold uppercase bg-white/5 text-zinc-500">A</span>
                                    )}
                                </td>
                                <td className="px-4 py-2">
                                    <span className={`px-2 py-0.5 rounded text-[10px] font-bold uppercase ${statusColors[email.status] || "bg-white/5 text-zinc-500"}`}>
                                        {email.status}
                                    </span>
                                </td>
                                <td className="px-4 py-2 text-zinc-500">{new Date(email.sentAt).toLocaleString()}</td>
                            </tr>
                        ))}
                    </tbody>
                </table>
            </div>
        </div>
    );
}
